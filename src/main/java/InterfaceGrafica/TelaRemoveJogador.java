    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente remover este Jogador?", "Remover Jogador", JOptionPane.YES_NO_CANCEL_OPTION);
        if (opcao == JOptionPane.YES_OPTION) {
            try {
                Jogador j = new Jogador();
                j.setId(Integer.parseInt(textoId.getText()));
                j.setNome_usuario(textoNome.getText());
                j.setEmail(textoEmail.getText());
                JogadorDAO dao = new JogadorDAO();
                dao.remover(j);
                textoId.setEnabled(true);
                botaoConsultar.setEnabled(true);
                botaoRemover.setEnabled(false);
                textoId.setText("");
                textoNome.setText("");
                textoEmail.setText("");
                JOptionPane.showMessageDialog(this, "Jogador removido com sucesso", "Remover Jogador", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Remover Jogador", JOptionPane.ERROR_MESSAGE);
            }
        }
        botaoRemover.transferFocus();
    }//GEN-LAST:event_botaoRemoverActionPerformed

    private void botaoConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoConsultarActionPerformed
        try {
            JogadorDAO dao = new JogadorDAO();
            Jogador  j = dao.obter(Integer.parseInt(textoId.getText()));
            textoNome.setText(j.getNome_usuario());
            textoEmail.setText(j.getEmail());
            textoId.setEnabled(false);
            botaoConsultar.setEnabled(false);
            botaoRemover.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Consultar Jogador", JOptionPane.ERROR_MESSAGE);
        }
        botaoConsultar.transferFocus();
    }//GEN-LAST:event_botaoConsultarActionPerformed

