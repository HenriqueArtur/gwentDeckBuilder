    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        int opcao = JOptionPane.showConfirmDialog(this, "Deseja realmente atualizar este cadastro?", "Atualizar Cadastro", JOptionPane.YES_NO_CANCEL_OPTION);
        if (opcao == JOptionPane.YES_OPTION) {
            try {
                Jogador j = new Jogador();
                j.setId(Integer.parseInt(textoId.getText()));
                j.setNome_usuario(textoNome.getText());
                j.setEmail(textoEmail.getText());
                j.setSenha(textoSenha.getText());
                JogadorDAO dao = new JogadorDAO();
                dao.atualizar(j);
                textoId.setEnabled(true);
                botaoConsultar.setEnabled(true);
                textoNome.setEnabled(false);
                textoEmail.setEnabled(false);
                textoSenha.setEnabled(false);
                botaoAtualizar.setEnabled(false);
                textoId.setText("");
                textoNome.setText("");
                textoEmail.setText("");
                textoSenha.setText("");
                JOptionPane.showMessageDialog(this, "Usu√°rio atualizado com sucesso", "Atualizar usuario", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Atualizar usuario", JOptionPane.ERROR_MESSAGE);
            }
        }
        botaoAtualizar.transferFocus();
    }//GEN-LAST:event_botaoAtualizarActionPerformed

    private void botaoConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoConsultarActionPerformed
        try {
            JogadorDAO dao = new JogadorDAO();
            Jogador j = dao.obter(Integer.parseInt(textoId.getText()));
            textoNome.setText(j.getNome_usuario());
            textoEmail.setText(j.getEmail());
            textoSenha.setText(j.getSenha());
            textoId.setEnabled(false);
            botaoConsultar.setEnabled(false);
            textoNome.setEnabled(true);
            textoEmail.setEnabled(true);
            textoSenha.setEnabled(true);
            botaoAtualizar.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Consultar usuario", JOptionPane.ERROR_MESSAGE);
        }
        botaoConsultar.transferFocus();
    }//GEN-LAST:event_botaoConsultarActionPerformed

